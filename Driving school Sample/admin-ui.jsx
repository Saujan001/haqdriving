/* ---------- Field ---------- */
function Field({ label, hint, error, count, children }) {
  return (
    <div className={"field" + (error ? " err" : "")}>
      {label && <label>{label}{hint && <span className="hint">{hint}</span>}{count != null && <span className="count">{count}</span>}</label>}
      {children}
      {error && <span className="err-msg">{error}</span>}
    </div>
  );
}

/* ---------- Avatar (colored initials) ---------- */
const AV_COLORS = ["#3B82F6","#10B981","#F59E0B","#8B5CF6","#EC4899","#06B6D4","#EF4444","#14B8A6"];
function avatarColor(str) {
  let h = 0; for (let i=0;i<str.length;i++) h = str.charCodeAt(i) + ((h<<5)-h);
  return AV_COLORS[Math.abs(h) % AV_COLORS.length];
}
function initials(name) {
  const p = name.trim().split(/\s+/);
  return ((p[0]?.[0]||"") + (p[1]?.[0]||"")).toUpperCase() || "?";
}

/* ---------- Modal ---------- */
function Modal({ title, icon, iconColor, onClose, children, footer, maxWidth }) {
  useEffect(() => {
    const h = e => { if (e.key === "Escape") onClose(); };
    window.addEventListener("keydown", h);
    return () => window.removeEventListener("keydown", h);
  }, [onClose]);
  return (
    <div className="modal-back" onMouseDown={e => { if (e.target === e.currentTarget) onClose(); }}>
      <div className="modal" style={maxWidth ? { maxWidth } : null}>
        <div className="modal-head">
          {icon && <div className="modal-icon" style={{ background: (iconColor||"#2563EB")+"1a", color: iconColor||"#2563EB" }}><Icon name={icon} /></div>}
          <h3>{title}</h3>
          <button className="x" onClick={onClose} aria-label="Close"><Icon name="x" /></button>
        </div>
        <div className="modal-body">{children}</div>
        {footer && <div className="modal-foot">{footer}</div>}
      </div>
    </div>
  );
}

/* ---------- Toast + Confirm context ---------- */
const UIContext = createContext(null);
const useUI = () => useContext(UIContext);

function UIProvider({ children }) {
  const [toasts, setToasts] = useState([]);
  const [confirm, setConfirm] = useState(null);
  const pushToast = useCallback((msg, type = "success") => {
    const id = Date.now() + Math.random();
    setToasts(t => [...t, { id, msg, type }]);
    setTimeout(() => setToasts(t => t.map(x => x.id === id ? { ...x, out: true } : x)), 2600);
    setTimeout(() => setToasts(t => t.filter(x => x.id !== id)), 2900);
  }, []);
  const askConfirm = useCallback((opts) => setConfirm(opts), []);
  return (
    <UIContext.Provider value={{ toast: pushToast, confirm: askConfirm }}>
      {children}
      <div className="toast-wrap">
        {toasts.map(t => (
          <div key={t.id} className={"toast " + t.type + (t.out ? " out" : "")}>
            <span className="ic"><Icon name={t.type === "success" ? "check" : "bell"} /></span>
            {t.msg}
          </div>
        ))}
      </div>
      {confirm && (
        <Modal title={confirm.title} icon={confirm.icon || "alert"} iconColor="#DC2626" maxWidth={420}
          onClose={() => setConfirm(null)}
          footer={<>
            <button className="btn btn--ghost" onClick={() => setConfirm(null)}>Cancel</button>
            <button className="btn btn--danger-solid" onClick={() => { confirm.onConfirm(); setConfirm(null); }}>{confirm.confirmLabel || "Delete"}</button>
          </>}>
          <p style={{ fontSize: 14, color: "var(--body)", lineHeight: 1.6 }}>{confirm.message}</p>
        </Modal>
      )}
    </UIContext.Provider>
  );
}

